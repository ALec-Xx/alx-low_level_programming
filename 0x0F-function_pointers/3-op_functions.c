#include "3-calc.h"

/**
 * op_add - function returns sum of a and b
 * @a: first integer
 * @b: second integer
 * Return: a + b
 **/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function returns the difference of a and b
 * @a: first integer
 * @b: second integer
 * Return: a - b
 **/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function returns the product of a and b
 * @a: First Integer
 * @b: Second Integer
 * Return: a * b
 **/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function returns the result of the division of a and b
 * @a: First int
 * @b: Second int
 * Return: a / b
 **/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function returns the remainder of the division of a and b
 * @a: First int
 * @b: Second int
 * Return: a % b
 **/
int op_mod(int a, int b)
{
	return (a % b);
}
